"""
Document read mode for the Simulation Workbench.

Read mode treats the current FreeCAD document as a viewer for a file that may be
regenerated by an external agent.  It watches the document's saved .FCStd path
and reloads the document after the file changes on disk.
"""
from __future__ import annotations

import json
import os
import time
from dataclasses import dataclass
from typing import Optional, Tuple


FileSignature = Tuple[float, int]
MODE_READ = "read"
MODE_WRITE = "write"
VALID_MODES = {MODE_READ, MODE_WRITE}
CONTROL_FILE_NAME = "read_mode_control.json"

_watcher: Optional["DocumentReadModeWatcher"] = None
_mode_subscribers = []


@dataclass(frozen=True)
class ReadModeConfig:
    poll_ms: int = 1000
    stable_delay_s: float = 0.75


def file_signature(path: str) -> Optional[FileSignature]:
    """Return the file modification signature used to detect external writes."""
    if not path or not os.path.isfile(path):
        return None
    stat = os.stat(path)
    return (stat.st_mtime, stat.st_size)


def same_path(left: str, right: str) -> bool:
    """Compare paths after normalising case and separators for the host OS."""
    if not left or not right:
        return False
    return os.path.normcase(os.path.abspath(left)) == os.path.normcase(os.path.abspath(right))


def _freecad_user_app_data_dir() -> str:
    try:
        import FreeCAD
        path = FreeCAD.getUserAppDataDir()
        if path:
            return os.path.abspath(path)
    except Exception:
        pass

    appdata = os.environ.get("APPDATA")
    if appdata:
        return os.path.join(appdata, "FreeCAD")
    return os.path.join(os.path.expanduser("~"), ".FreeCAD")


def control_file_path(path: Optional[str] = None) -> str:
    """Return the file used by agents to request read/write mode."""
    if path:
        return os.path.abspath(path)
    env_path = os.environ.get("SIMWORKBENCH_READ_MODE_CONTROL")
    if env_path:
        return os.path.abspath(env_path)
    return os.path.join(_freecad_user_app_data_dir(), "SimWorkbench", CONTROL_FILE_NAME)


def write_mode_request(
    mode: str,
    path: Optional[str] = None,
    source: str = "agent",
    control_file: Optional[str] = None,
) -> str:
    """Write a read/write mode request that a running FreeCAD GUI will poll."""
    mode = mode.lower().strip()
    if mode not in VALID_MODES:
        raise ValueError("mode must be one of: {}".format(", ".join(sorted(VALID_MODES))))

    request_path = control_file_path(control_file)
    parent = os.path.dirname(request_path)
    if parent:
        os.makedirs(parent, exist_ok=True)

    payload = {
        "mode": mode,
        "path": os.path.abspath(path) if path else None,
        "source": source,
        "updated_at": time.time(),
    }
    tmp_path = request_path + ".tmp"
    with open(tmp_path, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
        fh.write("\n")
    os.replace(tmp_path, request_path)
    return request_path


def read_mode_request(control_file: Optional[str] = None) -> Optional[dict]:
    """Read the latest external mode request, returning None when absent/invalid."""
    request_path = control_file_path(control_file)
    sig = file_signature(request_path)
    if sig is None:
        return None
    try:
        with open(request_path, "r", encoding="utf-8") as fh:
            payload = json.load(fh)
    except (OSError, ValueError):
        return None

    mode = str(payload.get("mode", "")).lower().strip()
    if mode not in VALID_MODES:
        return None
    requested_path = payload.get("path") or None
    if requested_path:
        requested_path = os.path.abspath(str(requested_path))
    return {
        "mode": mode,
        "path": requested_path,
        "source": payload.get("source", "agent"),
        "signature": sig,
        "control_file": request_path,
    }


def _import_qt_core():
    try:
        from PySide6 import QtCore
        return QtCore
    except Exception:
        pass
    try:
        from PySide2 import QtCore
        return QtCore
    except Exception:
        pass
    from PySide import QtCore
    return QtCore


def _active_document_path(freecad) -> Optional[str]:
    doc = getattr(freecad, "ActiveDocument", None)
    if doc is None:
        return None
    filename = getattr(doc, "FileName", "")
    if not filename:
        return None
    return os.path.abspath(filename)


def active_document_path() -> Optional[str]:
    try:
        import FreeCAD
    except Exception:
        return None
    return _active_document_path(FreeCAD)


def _matching_documents(freecad, path: str):
    for name, doc in freecad.listDocuments().items():
        filename = getattr(doc, "FileName", "")
        if filename and same_path(filename, path):
            yield name, doc


class DocumentReadModeWatcher:
    """Qt-timer backed watcher that reloads one saved FreeCAD document."""

    def __init__(self, freecad, freecad_gui, qt_core, path: str, config: ReadModeConfig):
        self.freecad = freecad
        self.freecad_gui = freecad_gui
        self.path = os.path.abspath(path)
        self.config = config
        self.last_signature = file_signature(self.path)
        self.pending_signature: Optional[FileSignature] = None
        self.pending_since: Optional[float] = None
        self.timer = qt_core.QTimer()
        self.timer.timeout.connect(self.poll)

    @property
    def running(self) -> bool:
        return bool(self.timer.isActive())

    def start(self) -> None:
        self.timer.start(self.config.poll_ms)

    def stop(self) -> None:
        self.timer.stop()

    def poll(self) -> None:
        sig = file_signature(self.path)
        if sig is None:
            return
        if self.last_signature is None:
            self.last_signature = sig
            return
        if sig == self.last_signature:
            self.pending_signature = None
            self.pending_since = None
            return

        now = time.time()
        if sig != self.pending_signature:
            self.pending_signature = sig
            self.pending_since = now
            return

        if self.pending_since is not None and now - self.pending_since >= self.config.stable_delay_s:
            self.reload("file changed")

    def reload(self, reason: str) -> None:
        sig = file_signature(self.path)
        if sig is None:
            self.freecad.Console.PrintWarning(
                "[SimWorkbench] Read mode file not found: {}\n".format(self.path)
            )
            return

        for name, doc in list(_matching_documents(self.freecad, self.path)):
            try:
                doc.setModified(False)
            except Exception:
                pass
            self.freecad.closeDocument(name)

        doc = self.freecad.openDocument(self.path)
        self.freecad.setActiveDocument(doc.Name)
        self.last_signature = sig
        self.pending_signature = None
        self.pending_since = None
        self._fit_view(doc.Name)
        self.freecad.Console.PrintMessage(
            "[SimWorkbench] Read mode reloaded ({}) at {}\n".format(
                reason, time.strftime("%H:%M:%S")
            )
        )

    def _fit_view(self, doc_name: str) -> None:
        try:
            gui_doc = self.freecad_gui.getDocument(doc_name)
            if gui_doc is not None and gui_doc.ActiveView is not None:
                gui_doc.ActiveView.viewAxometric()
                self.freecad_gui.SendMsgToActiveView("ViewFit")
        except Exception:
            pass


def is_read_mode_running() -> bool:
    return _watcher is not None and _watcher.running


def current_mode() -> str:
    return MODE_READ if is_read_mode_running() else MODE_WRITE


def watched_path() -> Optional[str]:
    return _watcher.path if _watcher is not None else None


def subscribe_mode_changes(callback) -> None:
    if callback not in _mode_subscribers:
        _mode_subscribers.append(callback)


def _notify_mode_change() -> None:
    mode = current_mode()
    path = watched_path()
    for callback in list(_mode_subscribers):
        try:
            callback(mode, path)
        except Exception:
            pass


def start_read_mode(path: Optional[str] = None, config: Optional[ReadModeConfig] = None) -> str:
    """Start read mode for a saved document path or the active FreeCAD document."""
    global _watcher

    import FreeCAD
    import FreeCADGui

    resolved = os.path.abspath(path) if path else _active_document_path(FreeCAD)
    if not resolved:
        return "Read mode needs an active saved document."
    if file_signature(resolved) is None:
        return "Read mode file does not exist: {}".format(resolved)

    if _watcher is not None:
        _watcher.stop()

    qt_core = _import_qt_core()
    _watcher = DocumentReadModeWatcher(
        FreeCAD,
        FreeCADGui,
        qt_core,
        resolved,
        config or ReadModeConfig(),
    )
    _watcher.start()
    if not list(_matching_documents(FreeCAD, resolved)):
        _watcher.reload("initial open")
    _notify_mode_change()
    return "Read mode watching {}".format(resolved)


def stop_read_mode() -> str:
    global _watcher
    if _watcher is None:
        return "Read mode is not running."
    path = _watcher.path
    _watcher.stop()
    _watcher = None
    _notify_mode_change()
    return "Read mode stopped for {}".format(path)


def set_document_mode(mode: str, path: Optional[str] = None) -> str:
    """Switch FreeCAD between read mode and normal write mode."""
    mode = mode.lower().strip()
    if mode == MODE_READ:
        return start_read_mode(path)
    if mode == MODE_WRITE:
        return stop_read_mode()
    return "Unknown document mode: {}".format(mode)


def toggle_read_mode() -> str:
    if is_read_mode_running():
        return set_document_mode(MODE_WRITE)
    return set_document_mode(MODE_READ)
